// Helper function to translate stock names
export function translateStockName(ticker: string, t: (key: string) => string, fallbackName?: string): string {
    const translationKey = `stock.${ticker.toUpperCase()}`;
    const translated = t(translationKey);
    
    // If translation exists and is different from the key, use it
    if (translated && translated !== translationKey) {
        return translated;
    }
    
    // Get current language
    const currentLang = i18n.language || 'zh';
    
    // In English environment, avoid showing Chinese names if no translation found
    if (currentLang === 'en' && fallbackName) {
        // Check if fallbackName contains Chinese characters
        const hasChinese = /[\u4e00-\u9fa5]/.test(fallbackName);
        if (hasChinese) {
            // Prefer ticker over Chinese name in English environment
            return ticker;
        }
    }
    
    // Otherwise use fallback name or ticker
    return fallbackName || ticker;
}

